import scipy.stats as stats

# Given data
sample_mean = 260  # Sample mean
population_mean = 270  # Population mean (claimed by the company)
population_stddev = 90  # Population standard deviation
sample_size = 18  # Sample size

# Calculate the test statistic (z)
z = (sample_mean - population_mean) / (population_stddev / (sample_size ** 0.5))
z

p_value = stats.norm.cdf(z)

# Print the p-value
print("The probability that 18 bulbs would have an average life of no more than 260 days:",p_value.round(3))
